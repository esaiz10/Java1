
public class Initials {
    public static void main(String[] args) {
        // Declare variables to hold the initials
        char firstInitial = 'E';
        char middleInitial = 'X';
        char lastInitial = 'S';

        // Display the initials with periods following each one
        System.out.println(firstInitial + "." + middleInitial + "." + lastInitial + ".");
    }
}